func (tc *ProductVideoController) Delete(c *gin.Context) {
	ID := c.Query("id")
	if ID == "" {
		c.JSON(http.StatusBadRequest, common.ErrorResponse{Message: "invalid ID format"})
		return
	}

	err := tc.ProductVideoUsecase.Delete(c, ID)
	if err != nil {
		c.JSON(http.StatusNotFound, common.ErrorResponse{Message: ID})
		return
	}

	c.JSON(http.StatusOK, common.SuccessResponse{
		Message: "Record deleted successfully",
	})
}

func (lc *ProductVideoController) FetchByID(c *gin.Context) {
	ProductVideoID := c.Query("id")
	if ProductVideoID == "" {
		c.JSON(http.StatusBadRequest, common.ErrorResponse{Message: "invalid ID format"})
		return
	}

	ProductVideo, err := lc.ProductVideoUsecase.FetchByID(c, ProductVideoID)
	if err != nil {
		c.JSON(http.StatusNotFound, common.ErrorResponse{Message: ProductVideoID})
		return
	}

	c.JSON(http.StatusOK, ProductVideo)
}

func (lc *ProductVideoController) Fetch(c *gin.Context) {

	ProductVideo, err := lc.ProductVideoUsecase.Fetch(c)
	if err != nil {
		c.JSON(http.StatusNotFound, common.ErrorResponse{Message: err.Error()})
		return
	}

	c.JSON(http.StatusOK, ProductVideo)
}
