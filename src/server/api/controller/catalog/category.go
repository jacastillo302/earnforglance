func (tc *CategoryController) Delete(c *gin.Context) {
	ID := c.Query("id")
	if ID == "" {
		c.JSON(http.StatusBadRequest, common.ErrorResponse{Message: "invalid ID format"})
		return
	}

	err := tc.CategoryUsecase.Delete(c, ID)
	if err != nil {
		c.JSON(http.StatusNotFound, common.ErrorResponse{Message: ID})
		return
	}

	c.JSON(http.StatusOK, common.SuccessResponse{
		Message: "Record deleted successfully",
	})
}

func (lc *CategoryController) FetchByID(c *gin.Context) {
	CategoryID := c.Query("id")
	if CategoryID == "" {
		c.JSON(http.StatusBadRequest, common.ErrorResponse{Message: "invalid ID format"})
		return
	}

	Category, err := lc.CategoryUsecase.FetchByID(c, CategoryID)
	if err != nil {
		c.JSON(http.StatusNotFound, common.ErrorResponse{Message: CategoryID})
		return
	}

	c.JSON(http.StatusOK, Category)
}

func (lc *CategoryController) Fetch(c *gin.Context) {

	Category, err := lc.CategoryUsecase.Fetch(c)
	if err != nil {
		c.JSON(http.StatusNotFound, common.ErrorResponse{Message: err.Error()})
		return
	}

	c.JSON(http.StatusOK, Category)
}
