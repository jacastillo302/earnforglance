func (tc *ProductReviewController) Delete(c *gin.Context) {
	ID := c.Query("id")
	if ID == "" {
		c.JSON(http.StatusBadRequest, common.ErrorResponse{Message: "invalid ID format"})
		return
	}

	err := tc.ProductReviewUsecase.Delete(c, ID)
	if err != nil {
		c.JSON(http.StatusNotFound, common.ErrorResponse{Message: ID})
		return
	}

	c.JSON(http.StatusOK, common.SuccessResponse{
		Message: "Record deleted successfully",
	})
}

func (lc *ProductReviewController) FetchByID(c *gin.Context) {
	ProductReviewID := c.Query("id")
	if ProductReviewID == "" {
		c.JSON(http.StatusBadRequest, common.ErrorResponse{Message: "invalid ID format"})
		return
	}

	ProductReview, err := lc.ProductReviewUsecase.FetchByID(c, ProductReviewID)
	if err != nil {
		c.JSON(http.StatusNotFound, common.ErrorResponse{Message: ProductReviewID})
		return
	}

	c.JSON(http.StatusOK, ProductReview)
}

func (lc *ProductReviewController) Fetch(c *gin.Context) {

	ProductReview, err := lc.ProductReviewUsecase.Fetch(c)
	if err != nil {
		c.JSON(http.StatusNotFound, common.ErrorResponse{Message: err.Error()})
		return
	}

	c.JSON(http.StatusOK, ProductReview)
}
