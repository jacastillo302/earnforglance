func (tc *OrderItemController) Delete(c *gin.Context) {
	ID := c.Query("id")
	if ID == "" {
		c.JSON(http.StatusBadRequest, common.ErrorResponse{Message: "invalid ID format"})
		return
	}

	err := tc.OrderItemUsecase.Delete(c, ID)
	if err != nil {
		c.JSON(http.StatusNotFound, common.ErrorResponse{Message: ID})
		return
	}

	c.JSON(http.StatusOK, common.SuccessResponse{
		Message: "Record deleted successfully",
	})
}

func (lc *OrderItemController) FetchByID(c *gin.Context) {
	OrderItemID := c.Query("id")
	if OrderItemID == "" {
		c.JSON(http.StatusBadRequest, common.ErrorResponse{Message: "invalid ID format"})
		return
	}

	OrderItem, err := lc.OrderItemUsecase.FetchByID(c, OrderItemID)
	if err != nil {
		c.JSON(http.StatusNotFound, common.ErrorResponse{Message: OrderItemID})
		return
	}

	c.JSON(http.StatusOK, OrderItem)
}

func (lc *OrderItemController) Fetch(c *gin.Context) {

	OrderItem, err := lc.OrderItemUsecase.Fetch(c)
	if err != nil {
		c.JSON(http.StatusNotFound, common.ErrorResponse{Message: err.Error()})
		return
	}

	c.JSON(http.StatusOK, OrderItem)
}
