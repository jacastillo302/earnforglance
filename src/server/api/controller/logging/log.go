func (tc *LogController) Delete(c *gin.Context) {
	ID := c.Query("id")
	if ID == "" {
		c.JSON(http.StatusBadRequest, common.ErrorResponse{Message: "invalid ID format"})
		return
	}

	err := tc.LogUsecase.Delete(c, ID)
	if err != nil {
		c.JSON(http.StatusNotFound, common.ErrorResponse{Message: ID})
		return
	}

	c.JSON(http.StatusOK, common.SuccessResponse{
		Message: "Record deleted successfully",
	})
}

func (lc *LogController) FetchByID(c *gin.Context) {
	LogID := c.Query("id")
	if LogID == "" {
		c.JSON(http.StatusBadRequest, common.ErrorResponse{Message: "invalid ID format"})
		return
	}

	Log, err := lc.LogUsecase.FetchByID(c, LogID)
	if err != nil {
		c.JSON(http.StatusNotFound, common.ErrorResponse{Message: LogID})
		return
	}

	c.JSON(http.StatusOK, Log)
}

func (lc *LogController) Fetch(c *gin.Context) {

	Log, err := lc.LogUsecase.Fetch(c)
	if err != nil {
		c.JSON(http.StatusNotFound, common.ErrorResponse{Message: err.Error()})
		return
	}

	c.JSON(http.StatusOK, Log)
}
