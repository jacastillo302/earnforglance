func (tc *ActivityLogController) Delete(c *gin.Context) {
	ID := c.Query("id")
	if ID == "" {
		c.JSON(http.StatusBadRequest, common.ErrorResponse{Message: "invalid ID format"})
		return
	}

	err := tc.ActivityLogUsecase.Delete(c, ID)
	if err != nil {
		c.JSON(http.StatusNotFound, common.ErrorResponse{Message: ID})
		return
	}

	c.JSON(http.StatusOK, common.SuccessResponse{
		Message: "Record deleted successfully",
	})
}

func (lc *ActivityLogController) FetchByID(c *gin.Context) {
	ActivityLogID := c.Query("id")
	if ActivityLogID == "" {
		c.JSON(http.StatusBadRequest, common.ErrorResponse{Message: "invalid ID format"})
		return
	}

	ActivityLog, err := lc.ActivityLogUsecase.FetchByID(c, ActivityLogID)
	if err != nil {
		c.JSON(http.StatusNotFound, common.ErrorResponse{Message: ActivityLogID})
		return
	}

	c.JSON(http.StatusOK, ActivityLog)
}

func (lc *ActivityLogController) Fetch(c *gin.Context) {

	ActivityLog, err := lc.ActivityLogUsecase.Fetch(c)
	if err != nil {
		c.JSON(http.StatusNotFound, common.ErrorResponse{Message: err.Error()})
		return
	}

	c.JSON(http.StatusOK, ActivityLog)
}
