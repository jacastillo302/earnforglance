func (tc *StoreController) Delete(c *gin.Context) {
	ID := c.Query("id")
	if ID == "" {
		c.JSON(http.StatusBadRequest, common.ErrorResponse{Message: "invalid ID format"})
		return
	}

	err := tc.StoreUsecase.Delete(c, ID)
	if err != nil {
		c.JSON(http.StatusNotFound, common.ErrorResponse{Message: ID})
		return
	}

	c.JSON(http.StatusOK, common.SuccessResponse{
		Message: "Record deleted successfully",
	})
}

func (lc *StoreController) FetchByID(c *gin.Context) {
	StoreID := c.Query("id")
	if StoreID == "" {
		c.JSON(http.StatusBadRequest, common.ErrorResponse{Message: "invalid ID format"})
		return
	}

	Store, err := lc.StoreUsecase.FetchByID(c, StoreID)
	if err != nil {
		c.JSON(http.StatusNotFound, common.ErrorResponse{Message: StoreID})
		return
	}

	c.JSON(http.StatusOK, Store)
}

func (lc *StoreController) Fetch(c *gin.Context) {

	Store, err := lc.StoreUsecase.Fetch(c)
	if err != nil {
		c.JSON(http.StatusNotFound, common.ErrorResponse{Message: err.Error()})
		return
	}

	c.JSON(http.StatusOK, Store)
}
